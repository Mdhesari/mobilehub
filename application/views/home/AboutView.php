					<h2>What is this place?</h2>
					<p>Bootsnipp is an element gallery for web designers and web developers, anybody who is using Bootstrap will find this website essential in their craft.</p>

					<p>Bootsnipp was created by <a target="_blank" href="http://maxoffsky.com">Maks Surguy</a> in September 2012 as a repository of useful HTML snippets for web developers using Bootstrap framework.</p>

					<p>It has been completely rebuilt a year later and released in October 2013 as a Bootstrap playground with more capabilities.</p>

					<p>Bootsnipp has been incredibly popular since its first day of existence, it has been on <a target="_blank" rel="nofollow" href="http://news.ycombinator.com/item?id=4520437">best of Hacker News</a> and Smashing Magazine <a target="_blank" rel="nofollow" href="https://www.facebook.com/smashmag">facebook page</a> and their <a target="_blank" rel="nofollow" href="https://twitter.com/smashingmag/status/250668741791215616">twitter feed.</a></p>

